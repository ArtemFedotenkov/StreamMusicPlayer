using System.Windows;
using StreamMusicPlayer.Models;

namespace StreamMusicPlayer.Services;

public static class LocalizationService
{
    public static AppLanguage CurrentLanguage { get; private set; } = AppLanguage.English;

    public static event EventHandler? LanguageChanged;

    public static void Apply(AppLanguage language)
    {
        CurrentLanguage = language;
        var translations = language switch
        {
            AppLanguage.Ukrainian => Ukrainian,
            AppLanguage.Russian => Russian,
            AppLanguage.Polish => Polish,
            _ => English
        };

        foreach (var (key, value) in English)
        {
            Application.Current.Resources[key] = value;
        }

        foreach (var (key, value) in translations)
        {
            Application.Current.Resources[key] = value;
        }

        LanguageChanged?.Invoke(null, EventArgs.Empty);
    }

    public static string T(string key)
    {
        if (Application.Current.Resources[key] is string value)
        {
            return value;
        }

        return English.TryGetValue(key, out var fallback) ? fallback : key;
    }

    public static string F(string key, params object[] args)
    {
        return string.Format(T(key), args);
    }

